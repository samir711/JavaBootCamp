public class Switch {
    public static void main(String[] args) {

        int star = 4;

        switch (star) {
            case  1:
                System.out.println("Rating is 1");
                break;

            case 4:
                System.out.println("Rating is 4");
                break;
            default:
               System.out.println("Rating can be only from 1-5");
               break;



        }
    }
}
